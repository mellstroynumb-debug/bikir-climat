const BASE_URL = 'http://localhost:3000'

async function importProducts() {
  console.log(`Importing ${products.length} GREE inverter products...`)

  try {
    const res = await fetch(`${BASE_URL}/api/import-products`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        products,
        category_override: 'invertornye'
      })
    })

    const data = await res.json()

    if (res.ok) {
      console.log(`Successfully imported ${data.imported} products!`)
      console.log('Titles:')
      data.titles.forEach((t, i) => console.log(`  ${i + 1}. ${t}`))
    } else {
      console.error('Import failed:', data.error)
    }
  } catch (err) {
    console.error('Request failed:', err.message)
  }
}

importProducts()
